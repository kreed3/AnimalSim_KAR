/**
 * The Walkable interface is implemented in the BrownBear and Goldfinch classes
 * 
 * @author Kaitlyn Reed
 * @version 4.12
 * AnimalSim Project
 * Spring 2020
 */
public interface Walkable {
	/**
	 * abstract walk method implemented in the BrownBear and Goldfinch classes
	 * @param direction: desired direction for the bear or bird to move in
	 */
	public void walk(int direction);
} // end Walkable
